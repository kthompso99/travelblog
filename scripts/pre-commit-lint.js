#!/usr/bin/env node

// ============================================
// Pre-Commit Lint - Unified validation checks
// ============================================
//
// Usage:
//   npm run pre-commit              # Standard checks
//   npm run pre-commit -- --test    # Include full test suite
//   npm run pre-commit -- --quiet   # Minimal output

import { execSync } from "child_process";
import fs from "fs";
import path from "path";

const args = process.argv.slice(2);
const RUN_TESTS = args.includes("--test");
const QUIET = args.includes("--quiet");

let exitCode = 0;
const issues = [];

function section(emoji, title) {
  if (!QUIET) console.log(`\n${emoji} ${title}`);
}

function pass(message) {
  if (!QUIET) console.log(`  ✓ ${message}`);
}

function warn(message) {
  if (!QUIET) console.log(`  ⚠ ${message}`);
  issues.push(message);
}

function fail(message) {
  console.error(`  ✗ ${message}`);
  issues.push(message);
  exitCode = 1;
}

// ============================================
// 1. Typography Check
// ============================================
section("📝", "Typography Check");
try {
  execSync("node scripts/tools/normalize-typography.js --dry-run", {
    stdio: "pipe",
    encoding: "utf-8"
  });
  pass("No curly quotes, em dashes, or en dashes found");
} catch (err) {
  const output = err.stdout || err.stderr || "";
  if (output.includes("Would normalize")) {
    warn("Typography issues found - run 'npm run normalize' to fix");
    if (!QUIET) console.log(output.trim().split("\n").map(l => `    ${l}`).join("\n"));
  } else {
    fail("Typography check failed");
  }
}

// ============================================
// 2. Config Validation
// ============================================
section("⚙️", "Config Validation");
try {
  execSync("node scripts/validate.js", {
    stdio: QUIET ? "pipe" : "inherit"
  });
  pass("All trip configs valid");
} catch (err) {
  fail("Config validation failed - see errors above");
}

// ============================================
// 3. Image Reference Validation
// ============================================
section("🖼️", "Image Reference Validation");
try {
  // Get all markdown files
  const contentPath = "content/trips";
  const mdFiles = [];

  function findMdFiles(dir) {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        // Skip images/ directories (working files only)
        if (entry.name !== "images" && entry.name !== "audits") {
          findMdFiles(fullPath);
        }
      } else if (entry.name.endsWith(".md") && entry.name !== "README.md") {
        mdFiles.push(fullPath);
      }
    }
  }

  if (fs.existsSync(contentPath)) {
    findMdFiles(contentPath);
  }

  if (mdFiles.length > 0) {
    execSync(`node scripts/validate-images.js ${mdFiles.join(" ")}`, {
      stdio: QUIET ? "pipe" : "inherit"
    });
    pass("All image references valid");
  } else {
    pass("No markdown files to validate");
  }
} catch (err) {
  fail("Image validation failed - broken references found");
}

// ============================================
// 4. Git Hook Preview
// ============================================
section("🪝", "Git Hook Status");
try {
  const staged = execSync("git diff --cached --name-only", { encoding: "utf-8" }).trim();

  if (!staged) {
    pass("No files staged yet");
  } else {
    const lines = staged.split("\n");
    const images = lines.filter(f => /content\/trips\/.*\/images\/.*\.(jpg|jpeg|png)$/.test(f));
    const contextDocs = lines.filter(f => /docs\/Content\/(Brand|Editorial-Standards|AntiAIWritingGuidelines)\.md$/.test(f));
    const mdFiles = lines.filter(f => /content\/trips\/.*\.md$/.test(f));

    if (images.length > 0) {
      pass(`${images.length} image(s) will be optimized by hook`);
    }

    if (contextDocs.length > 0) {
      pass(`Context-Combined.md will be auto-regenerated by hook`);
    }

    if (mdFiles.length > 0) {
      pass(`${mdFiles.length} markdown file(s) will be validated by hook`);
    }

    if (images.length === 0 && contextDocs.length === 0 && mdFiles.length === 0) {
      pass("No special hook actions needed");
    }
  }
} catch (err) {
  // Not a git repo or git command failed
  warn("Unable to check git status");
}

// ============================================
// 5. Test Suite (Optional)
// ============================================
if (RUN_TESTS) {
  section("🧪", "Test Suite");
  try {
    execSync("npm test", { stdio: QUIET ? "pipe" : "inherit" });
    pass("All tests passed");
  } catch (err) {
    fail("Test suite failed");
  }
}

// ============================================
// Summary
// ============================================
console.log("\n" + "=".repeat(50));
if (exitCode === 0 && issues.length === 0) {
  console.log("✅ Ready to commit\n");
} else if (exitCode === 0 && issues.length > 0) {
  console.log(`⚠️  ${issues.length} warning(s) - review before committing\n`);
} else {
  console.log(`❌ ${issues.length} issue(s) found - fix before committing\n`);
}

if (!RUN_TESTS && !QUIET) {
  console.log("💡 Run 'npm run pre-commit -- --test' to include full test suite\n");
}

process.exit(exitCode);
